package com.sonpc.LP0015_CarRental.tblCarCategory;

import java.io.Serializable;

/**
 *
 * @author ACER
 */
public class TblCarCategoryDTO implements Serializable {

    private int ID;
    private String name;
    private String description;

    public TblCarCategoryDTO() {
    }

    public TblCarCategoryDTO(int ID, String name, String description) {
        this.ID = ID;
        this.name = name;
        this.description = description;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
    
    
}
